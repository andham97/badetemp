import Vue from 'vue';
import Component from 'vue-class-component';
import GQLService, { ILocation, IWaterReading, IAirReading } from '@/services/GQLService';

interface IChartSerie {
    data: {
        x: number;
        y: number;
    }[];
    type: string;
    name?: string;
    visible?: boolean;
}

interface IChartOptions {
    series: IChartSerie[],
    title?: {
        text: string;
    };
    pane?: {
        size: string;
    };
    xAxis?: {
        type: string;
        dateTimeLabelFormats?: {
            day?: string;
        }
    }
}

@Component
export default class Overview extends Vue {
    public data: IChartSerie[] = [];
    public chartOptions: IChartOptions = {
        series: [] as IChartSerie[],
        title: {
            text: 'Overview',
        },
        pane: {
            size: '100%',
        },
        xAxis: {
            type: 'datetime',
            dateTimeLabelFormats: {
                day: '%e. %b'
            }
        }
    };

    private GQLService = new GQLService();

    async mounted() {
        const locations = (await this.GQLService.getQuery<{ locations: ILocation[] }>(`{ locations { name }}`)).locations;
        //const res = await Promise.all(locations.map(async (location) => this.GQLService.getQuery<{ waterReadings: IWaterReading[] }>(`{ waterReadings(location: "${location.name}") { time temp location { name }}}`)));
        const res = await Promise
            .all(locations
                .map(async (location) => 
                    this.GQLService.getQuery<{ airReadings: IAirReading[] }>(
                        `{ airReadings(location: "${location.name}") { time temp location { name }}}`
                    )
                )
            );
        if (res.length > 0) {
            this.data = [];
            res.forEach(line => {
                if (line.airReadings.length > 0) {
                    this.data.push({
                        data: line.airReadings.map(point => ({ x: new Date(Number(point.time as string)).getTime(), y: point.temp as number })),
                        type: 'line',
                        name: line.airReadings[0].location?.name,
                        visible: line.airReadings[0].location?.name == 'Rødstjønn',
                    });
                }
            });
            // console.log(this.$refs.chart.chart);
            // console.log(this.data);
            // this.$refs.chart.chart.addSeries(this.data[0]);
            this.chartOptions.series = this.data;
        }
    }
}

